import React from 'react';
import { render } from 'enzyme';
import { Col, Row } from '..';

describe('Grid', () => {
  it('should render Col', () => {
    const wrapper = render(
      <Col span="2" />
    );
    expect(wrapper).toMatchSnapshot();
  });

  it('should render Row', () => {
    const wrapper = render(
      <Row />
    );
    expect(wrapper).toMatchSnapshot();
  });

  it('renders wrapped Col correctly', () => {
    const MyCol = () => <Col span="12" />;
    const wrapper = render(
      <Row gutter={20}>
        <div>
          <Col span="12" />
        </div>
        <MyCol />
      </Row>
    );
    expect(wrapper).toMatchSnapshot();
  });
});
